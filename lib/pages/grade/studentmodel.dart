class Student {
  final String name;
  final String studentID;
  final String subject;
  final int assignmentScore;
  final int midTermScore;
  final int semesterScore;

  Student({
    required this.name,
    required this.studentID,
    required this.subject,
    required this.assignmentScore,
    required this.midTermScore,
    required this.semesterScore,
  });
}
